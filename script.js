function startTimer() {
  clearInterval(timer);
  
  // ุงุณุชุฑุฌุงุน ุงูููุช ุงููุชุจูู ูู localStorage ูู ููุฌูุฏ
  const savedTime = localStorage.getItem("timeLeft");
  const savedTimestamp = localStorage.getItem("timestamp");

  if (savedTime && savedTimestamp) {
    const elapsed = Math.floor((Date.now() - parseInt(savedTimestamp)) / 1000);
    timeLeft = parseInt(savedTime) - elapsed;
    if (timeLeft <= 0) {
      handleAnswer(null, "", puzzles[currentIndex].answer, true);
      return;
    }
  } else {
    timeLeft = 15;
  }

  timeDisplay.textContent = timeLeft;

  timer = setInterval(() => {
    timeLeft--;
    timeDisplay.textContent = timeLeft;

    localStorage.setItem("timeLeft", timeLeft);
    localStorage.setItem("timestamp", Date.now());

    if (timeLeft <= 0) {
      clearInterval(timer);
      handleAnswer(null, "", puzzles[currentIndex].answer, true);
    }
  }, 1000);
}

function showPuzzle() {
  const puzzle = puzzles[currentIndex];
  levelEl.innerHTML = `๐ ${puzzle.title} | โญ ุงูููุงุท: ${score} | ๐ ุฃุนูู ูุฑุญูุฉ: ${maxLevel}`;
  questionEl.textContent = puzzle.question;
  optionsEl.innerHTML = "";

  puzzle.options.forEach(option => {
    const btn = document.createElement("button");
    btn.className = "option-btn";
    btn.textContent = option;
    btn.onclick = () => handleAnswer(btn, option, puzzle.answer);
    optionsEl.appendChild(btn);
  });
  startTimer();
}

function handleAnswer(button, selected, correct, isTimeout = false) {
  clearInterval(timer);
  const buttons = document.querySelectorAll(".option-btn");
  buttons.forEach(btn => btn.disabled = true);

  if (selected === correct) {
    if (!isTimeout) score += 10;
    if (button) button.classList.add("correct");
  } else {
    if (button) button.classList.add("wrong");
    buttons.forEach(btn => {
      if (btn.textContent === correct) {
        btn.classList.add("correct");
      }
    });

  }
  if (selected === correct && !isTimeout) {
  correctSound.play();
  } else {
  wrongSound.play();
  }

  localStorage.removeItem("timeLeft");
  localStorage.removeItem("timestamp");

  nextBtn.classList.remove("hidden");
}

